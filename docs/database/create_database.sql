-- =============================================
-- Представление: полная информация о заказе
-- =============================================
CREATE VIEW v_Заказы_Полная AS
SELECT 
    з.заказ_id,
    з.статус,
    з.общая_сумма,
    з.дата_создания,
    з.дата_отгрузки,
    к.клиент_id,
    к.тип_клиента,
    к.фамилия,
    к.имя,
    к.отчество,
    к.наименование_организации,
    к.email,
    к.номер_телефона
FROM заказы з
INNER JOIN клиенты к ON з.клиент_id = к.клиент_id;
GO

-- =============================================
-- Представление: сборочные задания с деталями
-- =============================================
CREATE VIEW v_Сборочные_Задания_Полная AS
SELECT 
    сз.сборка_id,
    сз.название_конфигурации,
    сз.статус,
    сз.дата_начала,
    сз.дата_завершения,
    с.сотрудник_id,
    с.фамилия AS сотрудник_фамилия,
    с.имя AS сотрудник_имя,
    з.заказ_id,
    з.статус AS заказ_статус
FROM сборочные_задания сз
LEFT JOIN сотрудники с ON сз.сотрудник_id = с.сотрудник_id
LEFT JOIN заказы з ON сз.заказ_id = з.заказ_id;
GO

-- =============================================
-- Представление: остатки по всем складам
-- =============================================
CREATE VIEW v_Остатки_Полная AS
SELECT 
    к.компонент_id,
    к.наименование,
    к.тип,
    к.производитель,
    ск.склад_id,
    ск.название AS склад_название,
    ос.количество,
    ос.дата_обновления
FROM компоненты к
INNER JOIN остатки_на_складе ос ON к.компонент_id = ос.компонент_id
INNER JOIN склады ск ON ос.склад_id = ск.склад_id;
GO

-- =============================================
-- Триггер: аудит изменений для таблицы заказы (INSERT)
-- =============================================
CREATE TRIGGER trg_аудит_заказы_INSERT ON заказы
AFTER INSERT
AS
BEGIN
    SET NOCOUNT ON;
    
    INSERT INTO аудит_изменений (таблица, операция, пользователь_id, детали)
    SELECT 'заказы', 'INSERT', 1, 
           CONCAT('Создан заказ ID=', заказ_id, ', клиент_id=', клиент_id, ', сумма=', общая_сумма)
    FROM inserted;
END;
GO

-- =============================================
-- Триггер: аудит изменений для таблицы заказы (UPDATE)
-- =============================================
CREATE TRIGGER trg_аудит_заказы_UPDATE ON заказы
AFTER UPDATE
AS
BEGIN
    SET NOCOUNT ON;
    
    INSERT INTO аудит_изменений (таблица, операция, пользователь_id, детали)
    SELECT 'заказы', 'UPDATE', 1, 
           CONCAT('Обновлен заказ ID=', i.заказ_id, ', статус: ', d.статус, ' -> ', i.статус)
    FROM inserted i
    INNER JOIN deleted d ON i.заказ_id = d.заказ_id;
END;
GO

-- =============================================
-- Хранимая процедура: создание заказа
-- =============================================
CREATE PROCEDURE sp_СоздатьЗаказ
    @клиент_id INT,
    @пользователь_id INT
AS
BEGIN
    SET NOCOUNT ON;
    
    BEGIN TRANSACTION;
    
    DECLARE @заказ_id INT;
    
    INSERT INTO заказы (клиент_id, статус, общая_сумма)
    VALUES (@клиент_id, 'новый', 0.00);
    
    SET @заказ_id = SCOPE_IDENTITY();
    
    INSERT INTO аудит_изменений (таблица, операция, пользователь_id, детали)
    VALUES ('заказы', 'INSERT', @пользователь_id, CONCAT('Создан заказ ID=', @заказ_id));
    
    COMMIT TRANSACTION;
    
    SELECT @заказ_id AS заказ_id;
END;
GO

-- =============================================
-- Хранимая процедура: обновление остатков
-- =============================================
CREATE PROCEDURE sp_ОбновитьОстаток
    @компонент_id INT,
    @склад_id INT,
    @количество INT,
    @пользователь_id INT
AS
BEGIN
    SET NOCOUNT ON;
    
    IF EXISTS (SELECT 1 FROM остатки_на_складе WHERE компонент_id = @компонент_id AND склад_id = @склад_id)
    BEGIN
        UPDATE остатки_на_складе
        SET количество = количество + @количество,
            дата_обновления = GETDATE()
        WHERE компонент_id = @компонент_id AND склад_id = @склад_id;
        
        INSERT INTO аудит_изменений (таблица, операция, пользователь_id, детали)
        VALUES ('остатки_на_складе', 'UPDATE', @пользователь_id, 
                CONCAT('Компонент ', @компонент_id, ' на складе ', @склад_id, ': изменение на ', @количество));
    END
    ELSE
    BEGIN
        INSERT INTO остатки_на_складе (компонент_id, склад_id, количество)
        VALUES (@компонент_id, @склад_id, @количество);
        
        INSERT INTO аудит_изменений (таблица, операция, пользователь_id, детали)
        VALUES ('остатки_на_складе', 'INSERT', @пользователь_id, 
                CONCAT('Добавлен компонент ', @компонент_id, ' на склад ', @склад_id, ', количество: ', @количество));
    END
END;
